"""
Python translation of replication_analysis.do to reproduce key Wilcoxon signed-rank tests
for the focal claim: average market price generated by the UNIFORM mechanism differs from
(theory / FIXED treatment) price of 1.94.

All file IO is routed through /app/data as required.
"""

import json
from pathlib import Path
import pandas as pd
from scipy import stats

DATA_DIR = Path(__file__).resolve().parent
OUTPUT_DIR = Path('/app/data')
OUTPUT_DIR.mkdir(exist_ok=True, parents=True)

# Use the pre-computed group level dataset (combines round1 and round2 sessions)
full_path = DATA_DIR / 'fullsample_analysis.csv'

df = pd.read_csv(full_path)

# Helper that returns n, test statistic and p value

def wilcoxon_test(series, mu=1.94):
    # Drop missing
    series = series.dropna()
    # SciPy wilcoxon requires non-zero differences; we keep zeros to match Stata signrank behaviour
    # If all diffs are zero wilcoxon will raise an error; handle gracefully
    diffs = series - mu
    if (diffs == 0).all():
        return {'n': len(diffs), 'T': 0, 'p': 1.0}
    res = stats.wilcoxon(diffs)
    return {'n': len(diffs), 'T': res.statistic, 'p': res.pvalue}

results = {
    'all_groups': wilcoxon_test(df['price']),
    'no_dropout': wilcoxon_test(df.loc[df['dropout'] == 0, 'price']),
    'no_dropout_no_bankrupt': wilcoxon_test(df.loc[(df['dropout'] == 0) & (df['bankrupt'] == 0), 'price'])
}

print('Wilcoxon signed-rank tests of average group price vs 1.94')
for k, v in results.items():
    print(f"{k}: n={v['n']}, T={v['T']}, p={v['p']:.4f}")

# Save results to JSON for easy parsing by evaluation scripts
(out_file := OUTPUT_DIR / 'replication_results.json').write_text(json.dumps(results, indent=2))
print(f"Results written to {out_file.relative_to(Path('/app/data'))}")
